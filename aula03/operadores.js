let numero1 = 20;
let numero2 = 150;
//operador de comparação igual ==
console.log(numero1 == numero2);

//operador de comparação diferente !=
console.log(numero1 != numero2);

//=== tipo de dado e valores
console.log(numero1 === numero2);

//operador de comparação maior e menor <> e menor igual <= >=
console.log(numero1 > numero2);
console.log(numero1 >= numero2);
console.log(numero1 < numero2);
console.log(numero1 <= numero2);

