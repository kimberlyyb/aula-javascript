/* Crie um algoritimo ultlizando a estrutura condicional if-else que deve informar se o condutor foi multado por ultrapassar a velocidade seguindo as seguintes regras:

    Se a velocidade for maior que 80 o console deve retornar: Multado por excesso de velocidade.
    Se a velocidade for menor que 40, o console deve retornar: Multado por trafegar abaixo do limite permitido
    Caso contrário o console deve retornar: Boa viagem
*/
let velocidade = 40;

if(velocidade < 80){
    console.log("Multado por excesso de velocidade.");
} else if(velocidade < 40){
    console.log("Multado por trafegar abaixo do limite permitido");
} else{
    console.log("Boa viagem!");
}



